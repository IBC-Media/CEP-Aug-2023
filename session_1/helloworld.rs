fn main(){
    // printing a string into stdout
    println!("Hello World!");
    // creating variables
    let x = 32; // dynamic typing: do not specify the data type
    let y:i32 = 25; // static typing: specify the data type
    // printing variables
    println!("x is {}, y is {}", x, y);
    // mutable variables
    let mut z = 34;
    z = 5;
    // defining constants
    const AGE_LIMIT:u32 = 18;
    
}
